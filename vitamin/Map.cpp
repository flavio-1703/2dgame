Map::Map()
{
	dirt = TextureManager::loadTexture("../gfx/dirt.png");
	grass = TextureManager::loadTexture("../gfx/grass.png");
	water = TextureManager::loadTexture("../gfx/water.png");

	load_map(lvl1);

	src.x = src.y = 0;
	src.w = dst.w = 32;
	src.h = dst.h = 32;

	dst.x = dst.y = 0;
}


Map::~Map()
{

}

void Map::load_map(int arr[20][25])
{
	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			map[i][j] = arr[i][j];
		}
	}
}

void Map::draw_map()
{
	int type = 0;

	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			type = map[i][j];

			dst.x = j * 32;
			dst.y = i * 32;

			switch (type)
			{
			case 0:
			{
				TextureManager::Draw(water, src, dst);
				break;
			}
			case 1:
			{
				TextureManager::Draw(grass, src, dst);
				break;
			}
			case 2:
			{
				TextureManager::Draw(dirt, src, dst);
				break;
			}
			default:
				break;
			}
		}
	}
}
